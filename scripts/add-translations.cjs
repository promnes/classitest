// Helper to add keys only if they don't exist
function addKeys(obj, section, keys) {
  if (!obj[section]) obj[section] = {};
  for (const [key, val] of Object.entries(keys)) {
    if (typeof val === 'object' && !Array.isArray(val)) {
      if (!obj[section][key]) obj[section][key] = {};
      for (const [k2, v2] of Object.entries(val)) {
        if (obj[section][key][k2] === undefined) obj[section][key][k2] = v2;
      }
    } else {
      if (obj[section][key] === undefined) obj[section][key] = val;
    }
  }
}


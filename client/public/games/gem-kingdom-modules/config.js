export function loadProgress() {
  try {
    const raw = localStorage.getItem(KEYS.PROGRESS);
    if (raw) {
      const p = JSON.parse(raw);
      // Deep merge with defaults to ensure all nested keys exist
      const def = createDefaultProgress();
      return {
        ...def,
        ...p,
        skillData: { ...def.skillData, ...(p.skillData || {}) },
        settings: { ...def.settings, ...(p.settings || {}) },
        loginStreak: { ...def.loginStreak, ...(p.loginStreak || {}) },
      };
    }
  } catch (e) { /* ignore */ }
  return createDefaultProgress();
}

export function saveProgress(p) {
  try { localStorage.setItem(KEYS.PROGRESS, JSON.stringify(p)); } catch (e) { /* ignore */ }
}

/**
 * Inline translation helper — for 3-lang positional args.
 * Returns ar text for Arabic, pt for Portuguese, en for all others.
 * For full multi-language support, use t.key static translations.
 */
export function L(ar, en, pt) {
  if (LANG === 'ar') return ar;
  if (LANG === 'pt') return pt;
  return en;
}

/** Apply text direction (RTL for Arabic, Persian, Urdu) */
export function applyDir() {
  document.documentElement.dir = RTL_LANGS.includes(LANG) ? 'rtl' : 'ltr';
  document.documentElement.lang = LANG;
}
